# Sheet Upload
# ─────────────────────────────────────────────
def parse_sheet_url(url):
    m_id = re.search(r"/spreadsheets/d/([a-zA-Z0-9_-]+)", url)
    m_gid = re.search(r"gid=(\d+)", url)
    if not m_id:
        return None, None
    gid = m_gid.group(1) if m_gid else None
    return m_id.group(1), gid

def upload_to_sheets():
    print("=== Starting Google Sheets Integration ===")
    
    if not os.path.exists(STOCKS_CSV):
        print(f"Error: Stocks.csv not found at {STOCKS_CSV}")
        return
        
    # Read Stocks.csv
    stocks_to_update = []
    with open(STOCKS_CSV, newline='', encoding='utf-8-sig') as f:
        reader = csv.DictReader(f)
        for row in reader:
            sid = row.get('stock_id', '').strip()
            sheet_url = row.get('google_sheet_business', '').strip()
            if sid in INTEL_DATA:
                stocks_to_update.append({
                    'stock_id': sid,
                    'stock_name': row.get('stock_name', '').strip(),
                    'google_sheet_business': sheet_url
                })
                
    print(f"Found {len(stocks_to_update)} matching stocks in Stocks.csv.")
    
    # Authorize with gspread
    try:
        creds = Credentials.from_service_account_file(GCP_JSON, scopes=SCOPES)
        gc = gspread.authorize(creds)
        print("Successfully authenticated with Google Sheets service account.")
    except Exception as e:
        print(f"Failed to authenticate with gspread: {e}")
        return
        
    for item in stocks_to_update:
        sid = item['stock_id']
        name = item['stock_name']
        url = item['google_sheet_business']
        
        print(f"\nProcessing {sid} {name}...")
        
        if not url or url == 'xxx' or not url.startswith('https://'):
            print(f"  [Skipped] google_sheet_business URL is '{url}' (not a valid link).")
            continue
            
        doc_id, gid = parse_sheet_url(url)
        if not doc_id:
            print(f"  [Error] Failed to parse sheet URL: {url}")
            continue
            
        print(f"  Doc ID: {doc_id}, GID: {gid}")
        
        # Retrieve the business intelligence data
        data = INTEL_DATA[sid]
        
        # Prepare structured grid for Google Sheets (Key-Value table)
        sheet_rows = [
            ["項目", "情報內容"],
            ["報告日期", data['date']],
            ["產品營收佔比", data['product_ratio']],
            ["客戶與區域營收佔比", data['customer_ratio']],
            ["企業營運概況與基本介紹", data['overview']],
            ["主力產品與服務及獲利模式", data['core_profit']],
            ["未來展望", data['outlook']],
            ["潛在風險", data['risks']],
            ["競爭對手", data['competitor']],
            ["其他", data['other']]
        ]
        
        try:
            sh = gc.open_by_key(doc_id)
            # Find worksheet by GID or fallback to worksheet named "情報"
            worksheet = None
            if gid:
                try:
                    worksheet = sh.get_worksheet_by_id(int(gid))
                except Exception:
                    worksheet = None
            if not worksheet:
                try:
                    worksheet = sh.worksheet("情報")
                except Exception:
                    # Fallback to sheet1 if "情報" doesn't exist
                    worksheet = sh.sheet1
                    
            print(f"  Target Worksheet: {worksheet.title} (ID: {worksheet.id})")
            
            # Clear existing data from A1:B100
            worksheet.batch_clear(["A1:B100"])
            
            # Write new structured data
            worksheet.update(range_name="A1", values=sheet_rows, value_input_option='USER_ENTERED')
            print(f"  [Success] Overwrote Google Sheet worksheet '{worksheet.title}' with {len(sheet_rows)-1} sections.")
            
            # Apply basic formatting (Bold first row, adjust column widths if possible)
            try:
                # Bold headers
                worksheet.format("A1:B1", {
                    "textFormat": {"bold": True},
                    "horizontalAlignment": "CENTER",
                    "backgroundColor": {"red": 0.9, "green": 0.9, "blue": 0.9}
                })
                # Bold key column
                worksheet.format("A2:A11", {
                    "textFormat": {"bold": True},
                    "horizontalAlignment": "LEFT"
                })
                print("  [Success] Formatted columns and headers.")
            except Exception as fe:
                print(f"  [Warning] Formatting failed: {fe}")
                
        except Exception as e:
            print(f"  [Error] Failed to update Google Sheet for {sid}: {e}")

