# Markdown Report Generation Rules
# ─────────────────────────────────────────────
def generate_markdown(stock_id, data):
    # Rule 1: No "---" separators.
    # Rule 2: Big headings start at ##, subheadings (if any) are ### and ####.
    # Rule 3: Start with independent date.
    
    md = f"{data['date']}\n\n"
    
    md += f"## 產品營收佔比\n\n{data['product_ratio']}\n\n"
    md += f"## 客戶與區域營收佔比\n\n{data['customer_ratio']}\n\n"
    md += f"## 企業營運概況與基本介紹\n\n{data['overview']}\n\n"
    md += f"## 主力產品與服務及獲利模式\n\n{data['core_profit']}\n\n"
    md += f"## 未來展望\n\n{data['outlook']}\n\n"
    md += f"## 潛在風險\n\n{data['risks']}\n\n"
    md += f"## 競爭對手\n\n{data['competitor']}\n\n"
    md += f"## 其他\n\n{data['other']}\n"
    
    # Strip any occurrences of "---" to ensure compliance
    md = re.sub(r'^\s*---\s*$', '', md, flags=re.MULTILINE)
    return md

