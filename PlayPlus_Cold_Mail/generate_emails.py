def generate_emails(row):
    """根據一列資料產生 5 天的標題與內容。"""
    company = row["公司品牌簡稱"]
    website = row["官方網站"]
    industry = row["產業"]
    contact = row["聯絡人名稱"]
    desc = row["說明"]
    email = row["email"]

    # 判斷稱呼
    generic_contacts = ["官方", "窗口", "客服"]
    is_generic = any(k in contact for k in generic_contacts)
    greeting = "您好，"
    if not is_generic and contact:
        greeting = f"{contact}，您好，"

    # ---- 針對每家公司產生客製化觀察 ----
    emails = build_emails(company, website, desc, greeting, contact)
    return emails


