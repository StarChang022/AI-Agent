def run_update():
    """
    將 SUMMARIES 中的 AI 摘要寫回 Google Sheets『名單副本』分頁的「說明」欄位。
    只更新 SUMMARIES 中有對應公司且說明欄為空的列。
    """
    print("[步驟 1/2] 連接 Google Sheets，讀取『名單副本』現有資料...")
    gservice = gsh.get_service()
    rows, fieldnames = gsh.read_all_rows(gservice)
    print(f"讀取完成，共 {len(rows)} 列。")

    print("[步驟 2/2] 將 AI 摘要寫入「說明」欄位...")
    updated = 0
    for row in rows:
        name = row.get("公司品牌簡稱", "").strip()
        if name in SUMMARIES and not row.get("說明", "").strip():
            row["說明"] = SUMMARIES[name]
            updated += 1

    gsh.write_all_rows(gservice, rows, fieldnames)
    print(f"[GSheet] ✅ 已更新 {updated} 間公司的「說明」欄位")
    print(f"[GSheet] 連結：https://docs.google.com/spreadsheets/d/{gsh.SPREADSHEET_ID}/edit")

    # 刪除暫存檔
    temp_file = os.path.join(SCRIPT_DIR, "temp_profiles.json")
    if os.path.exists(temp_file):
        os.remove(temp_file)
        print(f"已刪除暫存檔: {temp_file}")

if __name__ == "__main__":
    run_update()
