def write_day1_to_sheet():
    print("[GSheet] 連接 Google Sheets...")
    service = gs.get_service()
    rows, fieldnames = gs.read_all_rows(service)
    
    # 確保 day1_title 與 day1_content 欄位存在
    if "day1_title" not in fieldnames:
        fieldnames.append("day1_title")
    if "day1_content" not in fieldnames:
        fieldnames.append("day1_content")
        
    updated_count = 0
    for row in rows:
        company_name = row.get("公司品牌簡稱", "").strip()
        email = row.get("email", "").strip()
        
        # 尋找對應的 MAIL_DATA
        for mail in MAIL_DATA:
            if mail["公司品牌簡稱"] == company_name and mail["email"] == email:
                row["day1_title"] = mail["title"]
                row["day1_content"] = mail["content"]
                updated_count += 1
                break
                
    print(f"[GSheet] 正在覆寫 {len(rows)} 列資料...")
    gs.write_all_rows(service, rows, fieldnames)
    print(f"[GSheet] ✅ 已成功更新 {updated_count} 筆 Day 1 郵件內容")

if __name__ == "__main__":
    write_day1_to_sheet()
