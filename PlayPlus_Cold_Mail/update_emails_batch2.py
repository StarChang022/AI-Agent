rows = []
with open(file_path, mode='r', encoding='utf-8') as f:
    reader = csv.reader(f)
    header = next(reader)
    rows.append(header)
    
    col_idx = {name: i for i, name in enumerate(header)}
    
    for row in reader:
        company_name = row[col_idx['公司品牌簡稱']]
        if company_name in data:
            for day in ['1', '7', '14', '30', '60']:
                title_col = f'day{day}_title'
                content_col = f'day{day}_content'
                
                if title_col in data[company_name]:
                    row[col_idx[title_col]] = data[company_name][title_col]
                    row[col_idx[content_col]] = data[company_name][content_col]
        rows.append(row)

with open(file_path, mode='w', encoding='utf-8', newline='') as f:
    writer = csv.writer(f)
    writer.writerows(rows)

