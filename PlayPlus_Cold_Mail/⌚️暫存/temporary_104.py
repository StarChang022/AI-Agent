def update_csv():
    print(f"Loading CSV file from: {LOCAL_CSV}")
    if not os.path.exists(LOCAL_CSV):
        print(f"Error: LOCAL_CSV not found!")
        return

    with open(LOCAL_CSV, 'r', encoding='utf-8') as f:
        reader = csv.reader(f)
        rows = list(reader)

    if not rows:
        print("Error: CSV is empty!")
        return

    header = rows[0]
    updated_rows = [header]
    
    updated_count = 0
    not_found_companies = set()

    for idx, row in enumerate(rows[1:], start=2):
        # Fill missing columns to guarantee length >= 25
        while len(row) < len(header):
            row.append('')

        company_name = row[0].strip()
        
        # We target I column (index 8)
        if company_name in MAPPED_DESCRIPTIONS:
            row[8] = MAPPED_DESCRIPTIONS[company_name]
            updated_count += 1
        else:
            not_found_companies.add(company_name)

        updated_rows.append(row)

    # Save CSV back
    with open(LOCAL_CSV, 'w', encoding='utf-8', newline='') as f:
        writer = csv.writer(f)
        writer.writerows(updated_rows)

    print(f"CSV updated successfully!")
    print(f"Total rows updated: {updated_count} (out of {len(rows) - 1} data rows)")
    if not_found_companies:
        print(f"Warning: The following companies in CSV did not match any entry in our map: {not_found_companies}")
    else:
        print("All rows successfully updated and matched.")

if __name__ == '__main__':
    update_csv()
