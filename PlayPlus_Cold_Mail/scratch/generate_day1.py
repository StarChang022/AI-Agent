# ── Helper: format content with proper line breaks ──
# Each line ends with <br>, paragraphs separated by a line containing only <br>
# This matches 範例2 format exactly:
#   您好，<br>
#   <br>
#   第一段...<br>
#   <br>
#   第二段...<br>
#   <br>
#   ...

def fmt(*paragraphs):
    """Join paragraphs with <br>\\n<br>\\n between them, each line ends with <br>"""
    lines = []
    for i, p in enumerate(paragraphs):
        lines.append(f"{p}<br>")
        if i < len(paragraphs) - 1:
            lines.append("<br>")
    return "\n".join(lines)

